"use client"

import { zodResolver } from "@hookform/resolvers/zod"
import { useForm } from "react-hook-form"
import * as z from "zod"
import { CalendarIcon, PlusCircle } from "lucide-react"
import { format } from "date-fns"
import { useEffect, useState } from "react"

import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import { Calendar } from "@/components/ui/calendar"
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from "@/components/ui/form"
import { Input } from "@/components/ui/input"
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from "@/components/ui/popover"
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "@/components/ui/select"
import { Combobox } from "@/components/ui/combobox"
import type { Expense } from "@/types"
import { useToast } from "@/hooks/use-toast"

const formSchema = z.object({
  amount: z.coerce.number().positive({ message: "Amount must be positive." }),
  currency: z.enum(["FMG", "Ariary"]),
  label: z.string().min(1, { message: "Label is required." }),
  date: z.date({ required_error: "A date is required." }),
})

type ExpenseFormValues = z.infer<typeof formSchema>

interface ExpenseFormProps {
  addExpense: (expense: Omit<Expense, "id" | "amountInFmg">) => Promise<void>
  uniqueLabels: string[]
}

export function ExpenseForm({ addExpense, uniqueLabels }: ExpenseFormProps) {
  const { toast } = useToast();
  const [isSubmitting, setIsSubmitting] = useState(false);

  const form = useForm<ExpenseFormValues>({
    resolver: zodResolver(formSchema),
    defaultValues: {
      amount: "" as any,
      currency: "FMG",
      label: "",
      date: undefined,
    },
  })
  
  useEffect(() => {
    // Set date only on client to avoid hydration mismatch
    form.setValue("date", new Date());
  }, [form]);

  const labelOptions = uniqueLabels.map(label => ({ value: label, label }));

  async function onSubmit(data: ExpenseFormValues) {
    setIsSubmitting(true);
    try {
      await addExpense(data)
      toast({
        title: "Expense Added",
        description: `${data.label} expense has been logged successfully.`,
      })
      form.reset({
        amount: "" as any,
        currency: 'FMG',
        label: '',
        date: new Date()
      })
    } catch (error) {
       toast({
        variant: "destructive",
        title: "Error",
        description: "Failed to add expense. Please try again.",
      })
    } finally {
      setIsSubmitting(false);
    }
  }

  return (
    <Form {...form}>
      <form onSubmit={form.handleSubmit(onSubmit)} className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-5 gap-4 items-end">
        <FormField
          control={form.control}
          name="amount"
          render={({ field }) => (
            <FormItem className="lg:col-span-1">
              <FormLabel>Amount</FormLabel>
              <FormControl>
                <Input type="number" placeholder="5000" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
        <FormField
          control={form.control}
          name="currency"
          render={({ field }) => (
            <FormItem className="lg:col-span-1">
              <FormLabel>Currency</FormLabel>
              <Select onValueChange={field.onChange} defaultValue={field.value}>
                <FormControl>
                  <SelectTrigger>
                    <SelectValue placeholder="Select currency" />
                  </SelectTrigger>
